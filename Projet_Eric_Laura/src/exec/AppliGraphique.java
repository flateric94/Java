    public void mort(){
       if (aventure.getHeros().getVie() <= 0){
           /*int reponse = JOptionPane.showMessageDialog(jFrame2,"Vous etes mort", "Fin", JOptionPane.DEFAULT_OPTION);
           if (reponse == JOptionPane.CLOSED_OPTION){
               System.exit(0);
           }*/
           String textes[]= {"revenir au menu principal", "quittez l'application"};
           int retour = JOptionPane.showOptionDialog(jFrame2, "Aïe, vous etes mort !", "Fin", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, textes, textes[0]);
           if(retour == JOptionPane.CLOSED_OPTION){
               //System.exit(0);
               jFrame2.setVisible(false);
               this.setVisible(true);
           }
           else if(retour == JOptionPane.YES_OPTION){
               jFrame2.setVisible(false);
               this.setVisible(true);
           }
           else{
               System.exit(0);
           }
           
       }
        
    }
    
