    private void ramasserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ramasserActionPerformed
        // TODO add your handling code here:
        Objet outil = jList3.getSelectedValue();
        
        if (poids_inventaire + outil.getPoids() > aventure.getHeros().getPoids_max()){
            jTextArea1.append("Votre sac est trop chargé, veuillez l'alléger pour pouvoir ramasser d'autres objets.\n");
        }
        else{
            aventure.getHeros().getInventaire().add(outil);
            aventure.getHeros().getLieu().objets.remove(outil);
            poids_inventaire += outil.poids;
            jProgressBar2.setValue(poids_inventaire*10);
            jTextArea1.append(aventure.getHeros() + " a ramassé un " + outil + ".\n");
        }
        
        mise_a_jour();
    }//GEN-LAST:event_ramasserActionPerformed

    private void utiliserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_utiliserActionPerformed
        // TODO add your handling code here:
        Objet outil = jList4.getSelectedValue();
    
        if (outil instanceof Arme){
            aventure.getHeros().equipe((Arme) outil);
            jTextArea1.append(aventure.getHeros() + " s'est équipé de " + outil + ".\n");
        }
        if (outil instanceof Armure){
           aventure.getHeros().equipe((Armure) outil);
           jTextArea1.append(aventure.getHeros() + " a mis une " + outil + ".\n");
        }
        if (outil instanceof Nourriture){
           aventure.getHeros().mange((Nourriture) outil);
           aventure.getHeros().getInventaire().remove(outil);
           poids_inventaire -= outil.poids;
           jProgressBar2.setValue(poids_inventaire*10);
           jTextArea1.append(aventure.getHeros() + " a mangé un " + outil + " et a gagné "+ ((Nourriture) outil).points_gagnes +" points de vie.\n");
        }
        
        if (outil instanceof Poison){
            poids_inventaire -= outil.poids;
           aventure.getHeros().mange((Poison) outil);
           aventure.getHeros().getInventaire().remove(outil);
           poids_inventaire -= outil.poids;
           jProgressBar2.setValue(poids_inventaire*10);
           jTextArea1.append(aventure.getHeros() + " a mangé un " + outil + "\n" + "Oups, c'était un poison, " + aventure.getHeros().effetpoison((Poison) outil)+ ".\n");
        }
        
        mise_a_jour();
    }//GEN-LAST:event_utiliserActionPerformed

