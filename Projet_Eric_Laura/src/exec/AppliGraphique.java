    public void fin(){
        if (aventure.getHeros().getLieu() == aventure.getDecor().arrivee){
            String textes[]= {"revenir au menu principal", "quittez l'application"};
           int retour = JOptionPane.showOptionDialog(jFrame2, "BRAVOOO, vous avez trouvé la sortie !", "Gagné", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, textes, textes[0]);
           if(retour == JOptionPane.CLOSED_OPTION){
               //System.exit(0);
               jFrame2.setVisible(false);
               this.setVisible(true);
           }
           else if(retour == JOptionPane.YES_OPTION){
               jFrame2.setVisible(false);
               this.setVisible(true);
           }
           else{
               System.exit(0);
           }
        }
    }
    
